#Calcular area de un triangulo

print("Ingrese la base de su triangulo:")
bas = int(input())      #'bas' DECLARADA como entera//Ingresando el valor de la base

print("Ingrese la altura de su triangulo")
altura = int(input())   #'altura' DECLARADA como entera//Ingresando el valor de la altura

#Forumula
area = (bas * altura)/2

#Impresion
print(f"Su area del triangulo es {area}")

